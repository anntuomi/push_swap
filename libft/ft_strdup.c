#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*copy;
	int		i;
	size_t	len;

	i = 0;
	len = ft_strlen(s1);
	if ((copy = ft_strnew(len)) == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}

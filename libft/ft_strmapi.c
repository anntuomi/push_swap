#include "libft.h"

char		*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	int				length;
	char			*fresh;

	length = ft_strlen(s);
	i = 0;
	if ((fresh = ft_strnew(length)) == NULL)
	{
		return (NULL);
	}
	while (s[i])
	{
		fresh[i] = (*f)(i, s[i]);
		i++;
	}
	fresh[i] = '\0';
	return (fresh);
}
